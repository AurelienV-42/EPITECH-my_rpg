static void build_file(short map[17][16], short collision[17][16])
{
	int fd = open("./newmap.map", O_WRONLY | O_CREAT, S_IRUSR | S_IWUSR);
	int ret;

	for (int y = 0; y < 17; y++)
		for (int x = 0; x < 16; x++)
			ret = write(fd, &map[y][x], sizeof(short));
	for (int y = 0; y < 17; y++)
		for (int x = 0; x < 16; x++)
			ret = write(fd, &collision[y][x], sizeof(short));
	ret = close(fd);
}

